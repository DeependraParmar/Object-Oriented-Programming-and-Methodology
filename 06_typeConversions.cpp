#include <iostream>
using namespace std;

int main(){
    int a = 8;
    float b = 9.67;
    char c = 'c';
    int d = 68;

    cout<<"Float a is: "<<float(a)<<endl;
    cout<<"Int b is: "<<int(b)<<endl;
    cout<<"Int c is: "<<int(c)<<endl;
    cout<<"Char d is: "<<char(d)<<endl;
    return 0;
}